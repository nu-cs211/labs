#pragma once

#include <stdbool.h>

// Make each character of the string s lowercase in-place.
// Input s must be an editable character array.
// Return nothing.
void make_lowercase(char* s);

// Make each character of the string s uppercase in-place.
// Input s must be an editable character array.
// Return nothing.
void make_uppercase(char* s);

// Add up the elements in an array.
// Returns the sum.
unsigned int sum_array(unsigned int* array, size_t array_len);

